def eggCount(num : int):
    """HAKMEM ALGORITH"""
    uCount = 0
    uCount = num-((num>>1) & 0b11011011011011011011011011011011) - ((num >> 2) & 0b1001001001001001001001001001001)
    return ((uCount + (uCount >> 3)) & 0b11000111000111000111000111000111) % 63

